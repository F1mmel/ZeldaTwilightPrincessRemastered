    public static ItemData GetItemData(int id)
    {
        foreach (ItemData a in Items)
        {
            if (Items.IndexOf(a) == id)
            {
                Archive archive = ArcReader.ReadObject(a.Name);

                List<RARC.File> files = ArcReader.GetFullFileData(archive);
                RARC.File actualFile = null;

                foreach (var f in files)
                {
                    if (f.ID == a.Bmd)
                    {
                        actualFile = f;
                        break;
                    }
                }

                if (a.Rot == Vector3.zero) a.Rot = new Vector3(-90f, 0, -11f);
                return a;
            }
        }

        return null;
    }

    public static BMD GetModelFromItemTable(ItemData data, Transform parent)
    {
        Archive archive = ArcReader.ReadObject(data.Name);

        List<RARC.File> files = ArcReader.GetFullFileData(archive);
        RARC.File actualFile = null;

        foreach (var f in files)
        {
            if (f.ID == data.Bmd)
            {
                actualFile = f;
                break;
            }
        }

        // Erstelle ein BMD-Objekt mit den erhaltenen Werten
        BMD bmd = BMDCreator.CreateModel(data.Name, actualFile.Name.Replace(".bmd", ""), parent);
        //bmd.SpriteTexture = modelData.Value.Sprite;
        bmd.Dialogue3DRotation = data.Rot;
        bmd.DialogueId = data.DialogueId;
        return bmd;
    }
