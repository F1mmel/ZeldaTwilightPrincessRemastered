[AttributeUsage(AttributeTargets.Field, AllowMultiple = false)]
public class ModelDataAttribute : Attribute
{
    public string Arc { get; }
    public string Bmd { get; }
    public Vector3 Rotation { get; }
    public int DialogueId { get; }

    public ModelDataAttribute(string arc, string bmd, string rotation, int dialogueId)
    {
        Arc = arc;
        Bmd = bmd;
        var values = rotation.Split(',');
        Rotation = new Vector3(
            float.Parse(values[0]),
            float.Parse(values[1]),
            float.Parse(values[2])
        );
        DialogueId = dialogueId;
    }
}
    
public static class EnumExtensions
{
    public static (string Arc, string Bmd, float rotX, float rotY, float rotZ, int dialogueId)? GetModelData(this ItemTable item)
    {
        var type = typeof(ItemTable);
        var member = type.GetMember(item.ToString()).FirstOrDefault();
        if (member != null)
        {
            var attribute = member.GetCustomAttribute<ModelDataAttribute>();
            if (attribute != null)
            {
                return (attribute.Arc, attribute.Bmd, attribute.Rotation.x, attribute.Rotation.y, attribute.Rotation.z, attribute.DialogueId);
            }
        }
        return null;
    }
    public static string GetBoneName(this SingleBoneConstraint boneConstraint)
    {
        // Retrieve the field info of the enum value
        FieldInfo fieldInfo = boneConstraint.GetType().GetField(boneConstraint.ToString());

        // Check if the BoneNameAttribute is applied
        if (fieldInfo != null && Attribute.GetCustomAttribute(fieldInfo, typeof(BoneNameAttribute)) is BoneNameAttribute attribute)
        {
            return attribute.Name; // Return the custom bone name
        }

        // Default to the enum name if no attribute is found
        return boneConstraint.ToString();
    }
